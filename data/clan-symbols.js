// Helper function to get clan symbol by name
export function getClanSymbol(clanName) {
  return clanSymbols[clanName] || null;
}

// Helper function to get all clan names
export function getAllClanNames() {
  return Object.keys(clanSymbols);
}

// Helper function to get clans by village
export function getClansByVillage(village) {
  return Object.values(clanSymbols).filter(clan => clan.village === village);
}

// Helper function to get clans with kekkei genkai
export function getClansWithKekkeiGenkai() {
  return Object.values(clanSymbols).filter(clan => clan.kekkeiGenkai !== null);
}

// Helper function to get symbol URL by clan name
export function getClanSymbolUrl(clanName) {
  const clan = getClanSymbol(clanName);
  return clan ? clan.symbolUrl : '';
}

